set -o errexit
set -o nounset
set -o pipefail

SCRIPT_ROOT=$(dirname "${BASH_SOURCE}")/..

# Files generated by generate-crd-yaml.sh
DEPLOY_CRD="${SCRIPT_ROOT}/deploy/vpa-v1-crd-gen.yaml"
CHARTS_CRD="${SCRIPT_ROOT}/charts/vertical-pod-autoscaler/crds/vpa-v1-crd-gen.yaml"

_tmp="${SCRIPT_ROOT}/_tmp"
TMP_DEPLOY_CRD="${_tmp}/deploy/vpa-v1-crd-gen.yaml"
TMP_CHARTS_CRD="${_tmp}/charts/vertical-pod-autoscaler/crds/vpa-v1-crd-gen.yaml"

cleanup() {
  rm -rf "${_tmp}"
}
trap "cleanup" EXIT SIGINT

cleanup

# Verify CRD files exist
if [[ ! -f "${DEPLOY_CRD}" ]]; then
  echo "Error: ${DEPLOY_CRD} does not exist"
  exit 1
fi
if [[ ! -f "${CHARTS_CRD}" ]]; then
  echo "Error: ${CHARTS_CRD} does not exist"
  exit 1
fi

# Backup the current CRD files
mkdir -p "${_tmp}/deploy"
mkdir -p "${_tmp}/charts/vertical-pod-autoscaler/crds"
cp "${DEPLOY_CRD}" "${TMP_DEPLOY_CRD}"
cp "${CHARTS_CRD}" "${TMP_CHARTS_CRD}"

# Regenerate the CRD files
"${SCRIPT_ROOT}/hack/generate-crd-yaml.sh"

echo "Diffing CRD files against freshly generated CRDs"
ret=0

diff -Naupr "${DEPLOY_CRD}" "${TMP_DEPLOY_CRD}" || ret=$?
diff -Naupr "${CHARTS_CRD}" "${TMP_CHARTS_CRD}" || ret=$?

# Restore the original files
cp "${TMP_DEPLOY_CRD}" "${DEPLOY_CRD}"
cp "${TMP_CHARTS_CRD}" "${CHARTS_CRD}"

if [[ $ret -eq 0 ]]
then
  echo "CRD files are up to date."
else
  echo "CRD files are out of date. Please run hack/generate-crd-yaml.sh"
  exit 1
fi
